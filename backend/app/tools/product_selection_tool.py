@tool
def analyze_competitors(product_name: str) -> str:
    """分析竞品，了解竞争对手的情况
    
    Args:
        product_name: 产品名称或关键词
    
    Returns:
        竞品分析结果
    """
    # 模拟竞品分析
    analysis = {
        "竞品数量": "15-20家",
        "平均价格": "¥89-129",
        "主要优势": ["品牌知名度高", "营销投入大", "评价数量多"],
        "主要劣势": ["产品同质化", "价格战激烈", "利润空间小"],
        "差异化建议": [
            "突出产品特色功能",
            "优化产品包装设计",
            "提供更好的售后服务",
            "打造品牌故事"
        ]
    }
    
    return f"## {product_name}竞品分析\n\n" + \
           f"- **竞品数量**: {analysis['竞品数量']}\n" + \
           f"- **平均价格**: {analysis['平均价格']}\n" + \
           f"- **主要优势**: {', '.join(analysis['主要优势'])}\n" + \
           f"- **主要劣势**: {', '.join(analysis['主要劣势'])}\n" + \
           f"- **差异化建议**:\n" + \
           "\n".join([f"  {i+1}. {s}" for i, s in enumerate(analysis['差异化建议'])]) + "\n"


